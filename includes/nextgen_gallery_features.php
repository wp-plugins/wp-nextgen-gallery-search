<div>
    <b>Description:</b>
    <p>
    This plugin is for who using Nexgen gallery plugin. our this plugin gives capability to search nexgen gallery and returns images. you can search by keyword.in free version it only supports titletext .
    
    This plugin gives 2 shortcodes.
    <ul>
    	<li>[usts-gallery-search-box] -&gt;this is for search box with search button. from here you can search with your search string.</li>
        <li>[usts-gallery-search] -&gt;this is for result page. search result will be shown in this shortcode page/post. and search result will have image slide show.</li>
    </ul>
    </p>
</div>
<div>
	<p>
    	<b>Features</b>
    </p>
	<ul>
    	<li>Nexgen image gallery search in all gallery</li>
        <li>This plugin works for any version of nexgen gallery</li>
        <li>it searchs only title in free version. but all field(description, tag) in pro version</li>
        <li>Search result shows image caption</li>
        <li>Search result have popup image slide show in pro version</li>
        <li>Most Important is this search plugin works with latest version of nextgen and will work for any future nextgen gallery plugin</li>
    </ul>
</div>
